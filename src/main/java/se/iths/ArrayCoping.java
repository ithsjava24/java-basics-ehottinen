package se.iths;

public class ArrayCoping {

    public static void main(String [] args) {
        int a =10;
        int b = a;

        System.out.println(a + " | " + b);
        System.out.println(",,,,,,,,");
        System.out.println(a + " | " + b);
        System.out.println();
        a++;

        System.out.println(a + " | " + b);
        System.out.println(",,,,,,,,");
        System.out.println(a + " | " + b);


    }

}
